void fillMap(char tab[mapY][mapX])
{
     for (int i = 0; i < mapY; i++)
     {
          for (int j = 0; j < mapX; j++)
          {
               if (i == 0 && j == 0 || i == 0 && j == 79 || i == 24 && j == 0 || i == 24 && j == 79)
               {
                    tab[i][j] = '#';
               }
               else
               {
                    tab[i][j] = '0';
               }
          }
     }
}

